    <h1>FAQ</h1>
    <h2>A bike rack tailgate?</h2>
    Yes! We hang out at the bike racks before the game. Because we love watching
    baseball and riding bicycles.
    <h2>Where?</h2>
    At the bike rack near Tailgate Haus. If available, we'll find a table to share
    nearby.
    <h2>When?</h2>
    Every first Friday Brewers home game. The next ride is May 14th. Here's the
    <a href="/schedule"> schedule</a>.
    <h2>What should I bring?</h2>
    You, your bike, and a lock.
    <h2>What if I don't have a bicycle?</h2>
    There is a Bubbler bicycle gate at the start and end of the ride. Learn more
    at
    <a href="https://bublrbikes.org/"> https://bublrbikes.org/</a>
    <h2>What about food and drink?</h2>
    BYOB, food, and drinks. Until posted otherwise leave the grill at home.
    <h2>
      I can't ride my bike today. I'm rooting for the other team. I don't know
      anything about baseball. I'm new to biking. I won't know anyone. Is this
      for me?
    </h2>
    If the Brew Cruisers tailgate seems like a good time to you, you belong at the
    Brew Cruisers tailgate. If you are made to feel otherwise, please let us know.
    <h2>Do you ride to the tailgate together?</h2>
    Yes! If you're coming from downtown or east of the ballpark, or you can get there
    an hour before first pitch, ride with us! Otherwise, ride on your own and meet
    us at the rack.
    <h2>How do I get home?</h2>
    A group of riders will retrace our route back to the starting point. There might
    be other groups riding home from there. More information on alternative transportation
    options is

    <a
      href="		https://www.mlb.com/brewers/ballpark/transportation/alternate-transportation
		">available on the Brewers website.</a
    >
    <h2>Do you sit together?</h2>
    Kinda! We plan to sit in section 202. Depending on on the crowd size bleacher
    tickets make it easiest to gather together at the game. Group ticketing requires
    at least a month advance and a headcount so perhaps a future game. You'll find
    more info here if it happens.
    <h2>Where's the bike parking?</h2>
    Compared to other ballparks there is limited accommodations for bicycles. There
    are bicycle racks in three locations. The Brew Cruisers ride plans to use the
    rack near the Tailgate Haus. There is a rack outside of the right field employee
    entrance and two surrounding Helfaer Field.
    <h2>Are the racks secure?</h2>
    The racks are within the ballpark premises and are generally patrolled by stadium
    staff. Similar to car tailgaters, bags and tailgate leftovers can be attached
    to our bikes, but it's best to leave any valuables at home or bring them into
    the stadium. We recommend you use a good bike lock whenever you lock up.
    <h2>What can I bring into the ballpark?</h2>
    Bags small bags (9" x 5"x 2" or smaller) clear totes (12" x 12" x 6" or smaller)
    diaper bags accompanied by a baby medical necessities no backpacks! Food in a
    clear storage bag no larger than 1 gallon Water one factory sealed bottle of
    water we haven't been turned away with an empty soft-plastic bike bottle to fill
    inside but it's technically against policy: be prepared to have to return it
    to your bike For more information see the
    <a href="https://www.mlb.com/brewers/ballpark/security"
      >2025 Brewers Bag Policy</a
    >
    and list of prohibited items.
    <h2>Is there a place I can check my bag?</h2>
    No. Other ballparks have started accommodating ticket buyers with bag checks,
    but this service is not available at American Family Field. You can leave stuff
    on your bike or you can bring your helmet in and use it as a purse.
    <h2>How long is the ride?</h2>
    Our standard Hank Aaron Trail route is a little less than 4 miles riding on Canal
    Street.
    <h2>What's the pace like?</h2>
    Chill and inclusive! We won't leave you behind.
    <h2>Where? When?</h2>
    Unless otherwise noted, we meet at the Great Roundabout of Walker's Point 75
    minutes before game time and roll out an hour before first pitch. For example
    on April 18th, meet at 5:55pm to roll out at 6:10. The most up to date information
    is available on our social accounts or at the<a href="/next-ride"
      >Next ride
    </a>
    page.
    <h2>Where can I find more bike rack tailgates?</h2>
    Chicago! The idea for this ride was inspired by the
    <a href="https://chicagobikesox.com/"> https://chicagobikesox.com/</a>.
